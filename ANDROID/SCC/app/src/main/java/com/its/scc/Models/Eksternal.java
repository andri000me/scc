package com.its.scc.Models;

public class Eksternal {
	String id_eksternal, nama, no_hp, akun_line, username, password, angkatan, foto;

	public String getId_eksternal() {
		return id_eksternal;
	}

	public void setId_eksternal(String id_eksternal) {
		this.id_eksternal = id_eksternal;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getNo_hp() {
		return no_hp;
	}

	public void setNo_hp(String no_hp) {
		this.no_hp = no_hp;
	}

	public String getAkun_line() {
		return akun_line;
	}

	public void setAkun_line(String akun_line) {
		this.akun_line = akun_line;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getAngkatan() {
		return angkatan;
	}

	public void setAngkatan(String angkatan) {
		this.angkatan = angkatan;
	}

	public String getFoto() {
		return foto;
	}

	public void setFoto(String foto) {
		this.foto = foto;
	}
}
